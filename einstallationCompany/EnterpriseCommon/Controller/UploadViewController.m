// 调用相机
- (void)pushImagePickerController {
    // 提前定位
    __weak typeof(self) weakSelf = self;
    [[TZLocationManager manager] startLocationWithSuccessBlock:^(NSArray<CLLocation *> *locations) {
        __strong typeof(weakSelf) strongSelf = weakSelf;
        strongSelf.location = [locations firstObject];
    } failureBlock:^(NSError *error) {
        __strong typeof(weakSelf) strongSelf = weakSelf;
        strongSelf.location = nil;
    }];
    
    UIImagePickerControllerSourceType sourceType = UIImagePickerControllerSourceTypeCamera;
    if ([UIImagePickerController isSourceTypeAvailable: UIImagePickerControllerSourceTypeCamera]) {
        self.imagePickerVc.sourceType = sourceType;
        NSMutableArray *mediaTypes = [NSMutableArray array];
        [mediaTypes addObject:(NSString *)kUTTypeImage];
        if (mediaTypes.count) {
            _imagePickerVc.mediaTypes = mediaTypes;
            self.uploadState = YES;
        }
        [self presentViewController:_imagePickerVc animated:YES completion:nil];
    } else {
        NSLog(@"模拟器中无法打开照相机,请在真机中使用");
    }
}


- (void)uploadImages:(NSString*) orderby{
    [SVProgressHUD show];
    AFHTTPSessionManager * manager = [AFHTTPSessionManager manager];
    manager.requestSerializer = [AFHTTPRequestSerializer serializer];
//    manager.responseSerializer = [[AFJSONResponseSerializer alloc]init];
    manager.responseSerializer = [AFHTTPResponseSerializer serializer];

       //添加一种支持的类型
 
   


    NSDictionary *dict = @{@"userid": [[NSUserDefaults standardUserDefaults] valueForKey:ENTERPRISE_USERID]
    };
    NSString *url = [ENTERPRISE_SERVER_HOST stringByAppendingString:UPLOAD_PICTURE_URL];

    [manager POST:url parameters:dict headers:NULL constructingBodyWithBlock:^(id<AFMultipartFormData>  _Nonnull formData) {
        UIImage *image;
   
            image = self.iv01.image;
      
        
        
        NSData *data = UIImagePNGRepresentation(image);
        NSDateFormatter *formatter = [[NSDateFormatter alloc] init];
        
        formatter.dateFormat = @"yyyyMMddHHmmss";
        
        NSString *fileName = [NSString stringWithFormat:@"%@.png",[formatter stringFromDate:[NSDate date]]];
        
        //上传文件参数
//        [formData appendPartWithFileData:data name:@"file" fileName:fileName mimeType:@"application/octet-stream"];
        [formData appendPartWithFileData:data name:@"file" fileName:fileName mimeType:@"image/png"];
        
        
        
        
    } progress:^(NSProgress * _Nonnull uploadProgress) {
        NSLog(@"------%f",1.0 * uploadProgress.completedUnitCount / uploadProgress.totalUnitCount);
        dispatch_async(dispatch_get_main_queue(), ^{
         
                self.label01.text = @"正在上传...";
            
        });
        
        
    } success:^(NSURLSessionDataTask * _Nonnull task, id  _Nullable responseObject) {
        NSLog(@"-----success-%@",responseObject);
        
        dispatch_async(dispatch_get_main_queue(), ^{
            if ([SVProgressHUD isVisible])[SVProgressHUD dismiss];
            [SVProgressHUD showSuccessWithStatus:@"上传成功"];
                self.label01.text = @"上传成功";
             });
        
        
    } failure:^(NSURLSessionDataTask * _Nullable task, NSError * _Nonnull error) {
        NSLog(@"-----error-%@",error);
       
        
        [SVProgressHUD showErrorWithStatus:@"数据上传失败"];
        dispatch_async(dispatch_get_main_queue(), ^{
            if ([SVProgressHUD isVisible])[SVProgressHUD dismiss];
            [SVProgressHUD showSuccessWithStatus:@"上传失败"];
                self.label01.text = @"上传失败";
            });
    }];
}

